class Planet {
    public int distance;

    public void planetInfo() {
        int moons = 2;
        System.out.println("Moons: " + moons);

        long planetID = 987654L;
        System.out.println("Planet ID: " + planetID);

        char type = 'G';
        System.out.println("Type: " + type);

        float gravity = 9.8f;
        System.out.println("Gravity: " + gravity);

        boolean habitable = false;
        System.out.println("Habitable: " + habitable);

        double mass = 5.97;
        System.out.println("Mass: " + mass);

        String name = "Earth";
        System.out.println("Name: " + name);

        distance = 150;
        System.out.println("Distance: " + distance);
    }
}

